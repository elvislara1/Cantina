package com.example.proyectocantina;

public class Comunidad {
    String nombre;
    String cabecera;
    String comentario;
    float valoracion;

    public Comunidad(String nombre, String cabecera, String comentario, float valoracion) {
        this.nombre = nombre;
        this.cabecera = cabecera;
        this.comentario = comentario;
        this.valoracion = valoracion;
    }
}
